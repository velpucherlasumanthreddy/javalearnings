package day04;

public class flags {
    public static void main(String[] args) {
        double num1=12.67;
        double num2=33.78;
        double num3=-54.01;

        System.out.printf("%+.2f\n",num1);
        System.out.printf("%+.2f\n",num2);
        System.out.printf("%+.2f\n",num3);

        System.out.printf("%,.2f\n",num1);
        System.out.printf("%,.2f\n",num2);
        System.out.printf("%,.2f\n",num3);

        System.out.printf("%(.2f\n",num1);
        System.out.printf("%(.2f\n",num2);
        System.out.printf("%(.2f\n",num3);

        System.out.printf("% .2f\n",num1);
        System.out.printf("% .2f\n",num2);
        System.out.printf("% .2f\n",num3);






    }
}
