package day04;

public class precision {
    public static void main(String[] args) {
        double num=9.99;
        System.out.printf("%f\n",num);
        System.out.printf("%.1f\n",num);
        System.out.printf("%.2f\n",num);
        System.out.printf("%.3f\n",num);
        System.out.printf("%.4f\n",num);
    }

}
