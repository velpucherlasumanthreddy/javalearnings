package day02;

public class augumentedAssignmentOperators {
    public static void main(String[] args){

        //AUGUMENTED ASSIGNMENT OPERATORS

        int x=10;
        int y=2;

        x += y;
        System.out.println("Addition :"+ x);
        x -= y;
        System.out.println("Subraction :"+ x);
        x *= y;
        System.out.println("Multiplication :"+ x);
        x /= y;
        System.out.println("Quotient :"+ x);
        x %= y;
        System.out.println("Modulus :"+ x);






    }
}
